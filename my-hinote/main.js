  async removeOriginalMarkupForEntries(entries) {
    if (!this.currentFilePath || entries.length === 0) return 0;
    const sourceFile = this.app.vault.getAbstractFileByPath(this.currentFilePath);
    if (!(sourceFile instanceof import_obsidian.TFile)) return 0;
    const markdownView = this.findMarkdownViewForCurrentFile();
    let content = await this.app.vault.read(sourceFile);
    let removed = 0;
    const removableEntries = entries.filter((entry) => entry.fullMatch && entry.fullMatch !== entry.content);
    for (const entry of removableEntries) {
      const nextContent = this.replaceEntryMarkup(content, entry);
      if (nextContent === content) continue;
      content = nextContent;
      removed++;
    }
    if (removed === 0) return 0;
    await this.app.vault.modify(sourceFile, content);
    if (markdownView) {
      markdownView.editor.setValue(content);
    }
    return removed;
  }

  findMarkdownViewForCurrentFile() {
    const leaves = this.app.workspace.getLeavesOfType("markdown");
    for (const leaf of leaves) {
      const view = leaf.view;
      if (view instanceof import_obsidian.MarkdownView && view.file && view.file.path === this.currentFilePath) {
        return view;
      }
    }
    return null;
  }

  replaceEntryMarkup(content, entry) {
    const fullMatch = entry.fullMatch;
    const replacement = entry.content;
    const exactIndex = content.indexOf(fullMatch);
    if (exactIndex === -1) return content;
    return content.slice(0, exactIndex) + replacement + content.slice(exactIndex + fullMatch.length);
  }

  async loadFlashcardsData() {
    const path = this.getFlashcardsJsonPath();
    if (await this.app.vault.adapter.exists(path)) {
      const content = await this.app.vault.adapter.read(path);
      try {
        return JSON.parse(content);
      } catch (e) {
        console.error("Failed to parse flashcards.json", e);
        return [];
      }
    }
    return [];
  }
