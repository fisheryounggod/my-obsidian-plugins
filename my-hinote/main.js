  extractMatches(content) {
    const entries = [];
    for (const rule of this.settings.customRegexes) {
      try {
        const regex = new RegExp(rule.regex, "gm");
        const matches = [...content.matchAll(regex)];
        for (const match of matches) {
          const innerContent = match.length > 1 ? match[1] : match[0];
          entries.push({
            ruleName: rule.name,
            template: rule.template,
            content: innerContent,
            fullMatch: match[0],
            index: match.index
          });
        }
      } catch (e) {
        console.error(`[Export] Error parsing regex for rule '${rule.name}':`, e);
      }
    }
    entries.sort((a, b) => a.index - b.index);
    return entries;
  }
