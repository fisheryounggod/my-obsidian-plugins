    const newFlashcards = flashcards.filter((f) => f.id !== cardId);
    const hasFlashcard = newFlashcards.length !== flashcards.length;
    if (hasComment) {
      delete fileComments[contentKey];
      if (Object.keys(fileComments).length === 0) {
        delete this.plugin.settings.comments[this.currentFilePath];
      }
    }
    if (hasFlashcard) {
      await this.saveFlashcardsData(newFlashcards);
    }
